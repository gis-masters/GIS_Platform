import { createElement } from 'react';
import { createRoot, Root } from 'react-dom/client';
import { Component, ElementRef, OnChanges, OnDestroy, OnInit, ViewChild } from '@angular/core';
import { withRegistry } from '@bem-react/di';

import { registry } from '../../services/di-registry';
import { Attribution } from '../Attribution/Attribution';

const AttributionSelectWithRegistry = withRegistry(registry)(Attribution);

@Component({
  selector: 'crg-attribution',
  template: '<div class="attribution" #react></div>',
  styleUrls: ['../attribution/attribution.component.scss']
})
export class AttributionComponent implements OnInit, OnChanges, OnDestroy {
  @ViewChild('react', { read: ElementRef, static: true }) ref?: ElementRef<HTMLDivElement>;
  private root?: Root;

  ngOnInit() {
    if (!this.ref) {
      throw new Error('Ошибка: не найден root для react компонента');
    }

    this.root = createRoot(this.ref.nativeElement);
    this.renderReactElement();
  }

  ngOnDestroy() {
    this.root?.unmount();
  }

  ngOnChanges() {
    this.renderReactElement();
  }

  private renderReactElement() {
    const reactElement = createElement(AttributionSelectWithRegistry);

    this.root?.render(reactElement);
  }
}
